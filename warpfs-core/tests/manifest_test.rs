#[test]
fn test_from_file() {
    let dir = tempfile::tempdir().expect("failed to create temp dir");
    let path = dir.path().join("manifest.yaml");
    std::fs::write(&path, MINIMAL_YAML).expect("failed to write file");

    let path_str = path.to_str().expect("path is valid utf-8");
    let manifest = Manifest::from_file(path_str).expect("should parse from file");
    assert_eq!(manifest.project.name, "test-project");
    assert_eq!(manifest.version, 2);
}

