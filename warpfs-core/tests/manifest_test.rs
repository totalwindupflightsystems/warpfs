#[test]
fn test_generated_marker_union() {
    let yaml = r#"
project:
  name: "marker-test"
discovery:
  generated_detection:
    enabled: true
    markers:
      - header: "DO NOT EDIT"
      - path: "*.gen.go"
      - header: "auto-generated"
"#;
    let manifest = Manifest::from_str(yaml).expect("should parse");
    let markers = &manifest.discovery.generated_detection.markers;
    assert_eq!(markers.len(), 3);
    assert!(markers[0].header.is_some());
    assert!(markers[0].path.is_none());
    assert!(markers[1].header.is_none());
    assert!(markers[1].path.is_some());
    assert!(markers[2].header.is_some());
}
