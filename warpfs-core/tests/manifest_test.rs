    // Sandbox defaults
    assert!(!manifest.sandbox.enabled);
    assert!(manifest.sandbox.engine.is_none());

    // Performance defaults
    assert_eq!(manifest.performance.cache.path, ".vfs/cache/");
    assert_eq!(manifest.performance.cache.max_size, "1GB");
    assert_eq!(manifest.performance.fuse.attr_timeout, 1.0);
    assert_eq!(manifest.performance.fuse.entry_timeout, 1.0);
