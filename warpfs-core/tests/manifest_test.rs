#[test]
fn test_parse_minimal() {
    let manifest = Manifest::from_str(MINIMAL_YAML).expect("minimal manifest should parse");
    assert_eq!(manifest.project.name, "test-project");
    assert_eq!(manifest.project.description, "");
}

#[test]
fn test_parse_complete() {
    let manifest = Manifest::from_str(COMPLETE_YAML).expect("complete manifest should parse");

    // Project
    assert_eq!(manifest.project.name, "my-project");
    assert_eq!(manifest.project.description, "Description surfaced to agents");

    // Interfaces
    assert!(manifest.interfaces.fuse.enabled);
    assert_eq!(manifest.interfaces.fuse.mount_point, "/mnt/vfs/project");
    assert!(!manifest.interfaces.ninep.enabled);
    assert_eq!(manifest.interfaces.ninep.listen, "0.0.0.0:5640");
    assert!(manifest.interfaces.mcp.enabled);
    assert_eq!(manifest.interfaces.mcp.transport, "stdio");
    assert_eq!(manifest.interfaces.mcp.port, 8766);

    // Repos
    assert_eq!(manifest.repos.len(), 2);
    assert_eq!(manifest.repos[0].name, "auth-service");
    assert_eq!(manifest.repos[0].git_ref, "main");
    assert!(manifest.repos[0].writable);
    assert_eq!(manifest.repos[0].manifest.as_deref(), Some(".vfs/repo-manifests/auth.yaml"));
    assert_eq!(manifest.repos[1].name, "shared-lib");
    assert_eq!(manifest.repos[1].git_ref, "v2.1.0");
    assert!(!manifest.repos[1].writable);

    // Backends
    assert_eq!(manifest.backends.s3.len(), 1);
    assert_eq!(manifest.backends.s3[0].bucket, "my-project-models");
    assert_eq!(manifest.backends.s3[0].ttl, 3600);
    assert!(!manifest.backends.s3[0].writable);
    assert!(manifest.backends.s3[0].lazy_load);
    assert_eq!(manifest.backends.remote.len(), 1);
    assert_eq!(manifest.backends.remote[0].auto_pull, Some(3600));
    assert_eq!(manifest.backends.local.len(), 1);
    assert!(manifest.backends.local[0].writable);

    // Metadata
    assert_eq!(manifest.metadata.namespaces.len(), 13);
    assert!(manifest.metadata.auto.hash);
    assert!(manifest.metadata.auto.complexity);

    // Graph
    assert_eq!(manifest.graph.edges, ".vfs/graph/edges.jsonl");
    assert!(manifest.graph.auto_discover.enabled);
    assert_eq!(manifest.graph.auto_discover.languages.len(), 9);
    assert_eq!(manifest.graph.impact.max_depth, 5);
    assert!(manifest.graph.cross_repo);
    assert_eq!(manifest.graph.max_edges_per_file, 100000);
    assert_eq!(manifest.graph.extensions.len(), 1);
    assert_eq!(manifest.graph.extensions[0].name, "docs");

    // Permissions
    assert_eq!(manifest.permissions.rules.len(), 6);
    assert_eq!(manifest.permissions.default_mode, "0644");
    assert_eq!(manifest.permissions.backends.len(), 1);
    assert_eq!(
        manifest.permissions.backends[0].name, "shared-lib"
    );

    // Triggers — test both single-string and list `on` forms
    assert_eq!(manifest.triggers.len(), 4);
    assert_eq!(manifest.triggers[0].name, "update-graph");
    assert_eq!(manifest.triggers[0].on, vec!["write", "delete"]);
    assert!(manifest.triggers[0].r#async);
    assert_eq!(manifest.triggers[1].name, "lint-go");
    assert_eq!(manifest.triggers[1].on, vec!["write"]); // single-string form
    assert_eq!(manifest.triggers[1].on_failure.as_deref(), Some("warn"));
    assert_eq!(manifest.triggers[2].name, "auto-test");
    assert_eq!(
        manifest.triggers[2].on_success.as_ref().unwrap().set_xattr.as_deref(),
        Some("user.vfs.last_tested={{ .Timestamp }}")
    );

    // Rules
    assert_eq!(manifest.rules.len(), 2);
    assert_eq!(manifest.rules[0].name, "stale-files");
    assert!(manifest.rules[0].query.contains("file_registry"));

    // Plugins
    assert_eq!(manifest.plugins.len(), 1);
    assert_eq!(manifest.plugins[0].hooks[0].priority, 10);
    assert_eq!(manifest.plugins[0].provides.as_ref().unwrap().edge_types.len(), 2);

    // Discovery
    assert!(manifest.discovery.feature_inference.enabled);
    assert_eq!(manifest.discovery.feature_inference.strategy, "directory");
    assert_eq!(manifest.discovery.test_association.patterns.len(), 4);
    assert_eq!(manifest.discovery.generated_detection.markers.len(), 5);
    assert_eq!(
        manifest.discovery.generated_detection.markers[0].header.as_deref(),
        Some("DO NOT EDIT")
    );
    assert_eq!(
        manifest.discovery.generated_detection.markers[3].path.as_deref(),
        Some("*.pb.go")
    );

    // Sandbox
    assert!(!manifest.sandbox.enabled);
    assert_eq!(manifest.sandbox.engine.as_deref(), Some("bubblewrap"));
    assert!(manifest.sandbox.isolate_network);
    assert_eq!(manifest.sandbox.writable_paths.len(), 2);

    // Performance
    assert_eq!(manifest.performance.cache.max_size, "1GB");
    assert_eq!(manifest.performance.fuse.attr_timeout, 1.0);
    assert_eq!(manifest.performance.fuse.max_read, 131072);
    assert_eq!(manifest.performance.duckdb.threads, 4);
    assert_eq!(manifest.performance.triggers.max_concurrent, 8);
}

